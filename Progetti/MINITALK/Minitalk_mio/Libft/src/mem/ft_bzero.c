#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*dhn;

	i = 0;
	dhn = (unsigned char *)s;
	while (i < n)
	{
		dhn[i] = 0;
		i++;
	}
}
