int	ft_atoi(const char *str)
{
	int n = 0;
	int sig = 1;
	int i = 0;
	
	if (str[0] == '-' || str[0] == '+')
	{
		if (str[0] == '-')
			sig = -1;
		i += 1;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
		n = (n * 10) + (str[i++] - '0');
	return(n * sig);
}